    private void txtbusKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtbusKeyPressed
         String[] equipo= {"cod","descripción","unidad","cantidad","costo"};
       //String[] Registro= new String[50];
       
       String sql = "Select * from transporte where cod like '%"+txtbus.getText()+"%'"
               +"or descripción like '%"+txtbus.getText()+"%'"
               +"or unidad like '%"+txtbus.getText()+"%'"
               +"or cantidad like '%"+txtbus.getText()+"%'"
               +"or costo like '%"+txtbus.getText()+"%'";
               
        DefaultTableModel modelo = new DefaultTableModel(null, equipo);
        Connection conn = getConnection();
        //Connection conect = conn.conexion();
        try {
            Statement st = (Statement) conn.createStatement();
            ResultSet rs =st.executeQuery(sql);
            while (rs.next()){
                 modelo.addRow(new Object[]{rs.getInt("cod"),rs.getString("descripción"),rs.getString("unidad"),rs.getInt("cantidad"),rs.getDouble("costo")});
            }
            jtbTransporte.setModel(modelo);
        } catch (Exception e) {
        }
                                      
    }//GEN-LAST:event_txtbusKeyPressed

    private void btnAddlistaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddlistaMouseClicked
     String descripcion=""; 
     String unidad="";
     int cantidad=0;
     Double costo=0.0;
        if(jtbTransporte.getSelectedRow()!=-1){ 
         int col =jtbTransporte.getSelectedRow();
         descripcion=jtbTransporte.getModel().getValueAt(col, 1).toString();
         unidad=jtbTransporte.getModel().getValueAt(col, 2).toString();
         cantidad=Integer.parseInt(jtbTransporte.getModel().getValueAt(col, 3).toString());
         costo=Double.parseDouble(jtbTransporte.getModel().getValueAt(col, 4).toString());
         DefaultTableModel AM = (DefaultTableModel)jtbAddTransporte.getModel();
         String cant=Integer.toString(cantidad);
         String precio2=String.valueOf(costo);
         String FilMat[] = {descripcion,unidad,cant,precio2};
         AM.addRow(FilMat);
     }
    
        
    }//GEN-LAST:event_btnAddlistaMouseClicked

