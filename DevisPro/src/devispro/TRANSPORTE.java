    private void mostrar(){
        DefaultTableModel modelo = new DefaultTableModel();
        ResultSet rs = Conexion.getTable("select * from transporte");
        modelo.setColumnIdentifiers(new Object[]{"cod","descripción","unidad","cantidad","costo"});
        try {
            while(rs.next()){
                modelo.addRow(new Object[]{rs.getInt("cod"),rs.getString("descripción"),rs.getString("unidad"),rs.getInt("cantidad"),rs.getDouble("costo")});
            }
            jtbTransporte.setModel(modelo);
        } catch (Exception e) {
            System.out.println(e);
        }
    } 

